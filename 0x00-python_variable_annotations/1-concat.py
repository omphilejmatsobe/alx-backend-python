#!/usr/bin/env python3
"""
    Module that takes str1 and a string str2 as
    arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
        Args:
            str1: The first string argument
            str1: The second string argument

        Return:
            concatenated string
    """

    return (str1 + str2)
